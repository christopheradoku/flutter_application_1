void main() async {
  // 1. Ensure Flutter bindings are initialized before calling native code (like Firebase)
  WidgetsFlutterBinding.ensureInitialized();
  
  // 2. Initialize Firebase with the correct platform options
  await Firebase.initializeApp(
    options: DefaultFirebaseOptions.currentPlatform,
  );

  // 3. Initialize the notification service & timezones
  await NotificationService.initialize();

  // 4. Boot the app wrapped in the state management provider
  runApp(
    ChangeNotifierProvider(
      create: (context) => FarmProvider(),
      child: const PoultryProApp(),
    ),
  );
}

